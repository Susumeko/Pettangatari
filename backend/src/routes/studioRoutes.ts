router.post('/assets/delete-generated', async (request, response) => {
  const body =
    request.body && typeof request.body === 'object' && !Array.isArray(request.body)
      ? (request.body as Record<string, unknown>)
      : null;
  const filePathsRaw = Array.isArray(body?.filePaths) ? body.filePaths : [];
  const resolvedPaths = Array.from(
    new Set(
      filePathsRaw
        .map((value) => (typeof value === 'string' ? resolveGeneratedAssetPathForDeletion(value) : null))
        .filter((value): value is string => Boolean(value)),
    ),
  );

  if (filePathsRaw.length === 0) {
    return response.status(400).json({ error: 'No generated files were provided.' });
  }

  if (resolvedPaths.length === 0) {
    return response.status(400).json({ error: 'No valid generated files were provided.' });
  }

  try {
    await Promise.all(
      resolvedPaths.map(async (filePath) => {
        await rm(filePath, { force: true });
      }),
    );
    return response.json({ ok: true, deletedCount: resolvedPaths.length });
  } catch (error) {
    return response.status(500).json({
      error: error instanceof Error ? error.message : 'Failed to delete generated asset files.',
    });
  }
});

