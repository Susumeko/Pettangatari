function isPathInsideDirectory(targetPath: string, directoryPath: string): boolean {
  const resolvedTarget = path.resolve(targetPath);
  const resolvedDirectory = path.resolve(directoryPath);
  const relative = path.relative(resolvedDirectory, resolvedTarget);
  return relative === '' || (!relative.startsWith('..') && !path.isAbsolute(relative));
}

function resolvePackageFilePath(fileName: string, storedFilePath: string): string {
  const normalizedFileName = path.basename(fileName || '').trim();
  const preferredPath = path.resolve(appConfig.packageOutputPath, normalizedFileName);

  const normalizedStoredPath = asOptionalString(storedFilePath)?.trim() || '';
  if (!normalizedStoredPath) {
    return preferredPath;
  }

  const resolvedStoredPath = path.resolve(normalizedStoredPath);
  if (
    isPathInsideDirectory(resolvedStoredPath, appConfig.packageOutputPath) &&
    path.basename(resolvedStoredPath).toLowerCase() === normalizedFileName.toLowerCase()
  ) {
    return resolvedStoredPath;
  }

  return preferredPath;
}

async function addDirectoryToZip(archive: JSZip, sourceDirectory: string, archiveDirectory: string): Promise<void> {
  const entries = await readdir(sourceDirectory, { withFileTypes: true });
  for (const entry of entries) {
    const sourcePath = path.join(sourceDirectory, entry.name);
    const archivePath = `${archiveDirectory}/${entry.name}`.replace(/\\/g, '/');
    if (shouldSkipPackageArchiveEntry(archivePath, entry)) {
      continue;
    }
    if (entry.isDirectory()) {
      await addDirectoryToZip(archive, sourcePath, archivePath);
      continue;
    }
    if (!entry.isFile()) {
      continue;
    }

    archive.file(archivePath, await readFile(sourcePath));
  }
}

function shouldSkipPackageArchiveEntry(archivePath: string, entry: { isDirectory: () => boolean }): boolean {
  return entry.isDirectory() && /^characters\/[^/]+\/sprites\/cg-[^/]+$/i.test(archivePath);
}

function parsePackageZipData(value: string): Buffer | null {
  if (!value) {
    return null;
  }

  const match = value.match(/^data:[^;]+;base64,(.+)$/i);
  const base64 = match ? match[1] : value;
  if (!base64) {
    return null;
  }

  try {
    const buffer = Buffer.from(base64, 'base64');
    return buffer.length > 0 ? buffer : null;
  } catch {
    return null;
  }
}

