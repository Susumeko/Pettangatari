function resolveGeneratedAssetPathForDeletion(filePath: string): string | null {
  const trimmedPath = filePath.trim();
  if (!trimmedPath) {
    return null;
  }

  const generatedRoot = path.resolve(appConfig.autogeneratedSpritesPath);
  const resolvedPath = path.resolve(trimmedPath);
  const relativePath = path.relative(generatedRoot, resolvedPath);
  if (!relativePath || relativePath.startsWith('..') || path.isAbsolute(relativePath)) {
    return null;
  }

  return resolvedPath;
}

