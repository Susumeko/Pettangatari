router.post('/packages', async (request, response) => {
  const scenarioId = asOptionalString((request.body as Record<string, unknown> | null)?.scenarioId)?.trim();
  if (!scenarioId) {
    return response.status(400).json({ error: 'Scenario is required.' });
  }

  const requestedPackageName =
    asOptionalString((request.body as Record<string, unknown> | null)?.packageName)?.trim() || '';

  try {
    const state = await getStudioState();
    const scenario = state.scenarios.find((entry) => entry.id === scenarioId);
    if (!scenario) {
      return response.status(404).json({ error: 'Scenario not found.' });
    }

    const character = state.characters.find((entry) => entry.id === scenario.characterId);
    if (!character) {
      return response.status(404).json({ error: 'Scenario character not found.' });
    }

    await mkdir(appConfig.packageOutputPath, { recursive: true });
    const packageName = requestedPackageName || `${scenario.name} Package`;
    const fileName = `${slugifyFileName(packageName)}-${Date.now()}.zip`;
    const filePath = path.join(appConfig.packageOutputPath, fileName);
    const characterDirectory = path.join(appConfig.fileCharactersPath, path.basename(character.id));
    const scenarioDirectory = path.join(appConfig.fileScenesPath, path.basename(scenario.id));

    if (!isPathInsideDirectory(characterDirectory, appConfig.fileCharactersPath)) {
      return response.status(400).json({ error: 'Character folder path is invalid.' });
    }
    if (!isPathInsideDirectory(scenarioDirectory, appConfig.fileScenesPath)) {
      return response.status(400).json({ error: 'Scenario folder path is invalid.' });
    }

    const packageMetadata = {
      format: 'pettangatari-package',
      archiveFormat: 'folders-zip',
      version: 4,
      packageName,
      exportedAt: new Date().toISOString(),
      characterId: character.id,
      scenarioId: scenario.id,
      sillyTavernCharacterCardName: character.cardName,
      packageBannerDataUrl: scenario.bannerDataUrl || '',
    };

    const archive = new JSZip();
    const sillyTavernCard = await findSillyTavernCardForStudioCharacter(character);
    archive.file(PACKAGE_METADATA_FILE, `${JSON.stringify(packageMetadata, null, 2)}\n`);
    archive.file(SILLY_TAVERN_CHARACTER_CARD_FILE, `${JSON.stringify(sillyTavernCard, null, 2)}\n`);
    await addDirectoryToZip(archive, characterDirectory, `characters/${character.id}`);
    await addDirectoryToZip(archive, scenarioDirectory, `scenes/${scenario.id}`);
    const archiveBuffer = await archive.generateAsync({
      type: 'nodebuffer',
      compression: 'DEFLATE',
      compressionOptions: { level: 6 },
    });
    await writeFile(filePath, archiveBuffer);

    const packageRecord = await addPackageRecord({
      name: packageName,
      bannerDataUrl: scenario.bannerDataUrl || '',
      scenarioId: scenario.id,
      characterId: character.id,
      fileName,
      filePath,
      source: 'created',
    });

    return response.json({ package: packageRecord });
  } catch (error) {
    return response.status(500).json({
      error: error instanceof Error ? error.message : 'Failed to create package.',
    });
  }
});

router.post('/packages/import', async (request, response) => {
  const body = request.body as Record<string, unknown> | null;
  const fileName = asOptionalString(body?.fileName)?.trim() || `package-${Date.now()}.zip`;
  const packageData = asOptionalString(body?.packageData)?.trim() || '';
  const packageBuffer = parsePackageZipData(packageData);
  if (!packageBuffer) {
    return response.status(400).json({ error: 'Valid package ZIP is required.' });
  }

  try {
    let archive: JSZip;
    try {
      archive = await JSZip.loadAsync(packageBuffer);
    } catch {
      return response.status(400).json({ error: 'Package ZIP could not be read.' });
    }
    const metadata = await readPackageMetadata(archive);
    const extractionPlan = buildPackageExtractionPlan(archive);
    if (!extractionPlan) {
      return response.status(400).json({ error: 'Package ZIP must contain one character folder and one scenario folder.' });
    }
    try {
      await validatePackageManifests(extractionPlan);
    } catch (error) {
      return response.status(400).json({
        error: error instanceof Error ? error.message : 'Package manifests are invalid.',
      });
    }
    const sillyTavernCard = await readPackageSillyTavernCharacterCard(archive);

    await mkdir(appConfig.packageOutputPath, { recursive: true });
    const storedFileName = `${slugifyFileName(metadata.packageName || fileName.replace(/\.zip$/i, ''))}-${Date.now()}.zip`;
    const storedFilePath = path.join(appConfig.packageOutputPath, storedFileName);
    await writeFile(storedFilePath, packageBuffer);

    if (sillyTavernCard) {
      await importSillyTavernCharacterCard(sillyTavernCard);
    }

    await extractPackageFolders(extractionPlan);
    const importedState = await getStudioState();
    const character = importedState.characters.find((entry) => entry.id === extractionPlan.characterId);
    const scenario = importedState.scenarios.find((entry) => entry.id === extractionPlan.scenarioId);
    if (!character || !scenario) {
      return response.status(400).json({ error: 'Imported package folders did not contain valid manifests.' });
    }

    const packageRecord = await addPackageRecord({
      name: metadata.packageName || `${scenario.name} Package`,
      bannerDataUrl: scenario.bannerDataUrl || metadata.packageBannerDataUrl || '',
      scenarioId: scenario.id,
      characterId: character.id,
      fileName: storedFileName,
      filePath: storedFilePath,
      source: 'imported',
    });

    return response.json({
      package: packageRecord,
      character,
      scenario,
    });
  } catch (error) {
    return response.status(500).json({
      error: error instanceof Error ? error.message : 'Failed to import package.',
    });
  }
});

