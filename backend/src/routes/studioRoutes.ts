function parseCharacterPayload(
  body: unknown,
): {
  id?: string;
  name: string;
  cardName: string;
  accentColor: string;
  suggestedAffinityPositiveMaximum?: number;
  suggestedAffinityNegativeMaximum?: number;
  suggestedLustMaximum?: number;
  characterNameFontId?: string;
  characterNameColor?: string;
  blipSound?: string;
  dialogueQuoteFontId?: string;
  dialogueQuoteAnimationPreset?: string;
  dialogueQuoteAnimationSpeed?: number;
  dialogueQuoteAnimationColor?: string;
  sprites: Record<string, string[]>;
  spriteDepthMaps: Record<string, string[]>;
  spriteAnimationFrames: Record<string, { closedEyes: string[]; openMouth: string[] }>;
  customReactions: Array<{
    name: string;
    sprites: string[];
    depthMaps?: string[];
    animationFrames?: { closedEyes: string[]; openMouth: string[] };
    triggers: string[];
  }>;
  automaticGeneration: CharacterAutomaticGenerationRecord;
  spriteZones: Record<
    string,
    Array<{ id: string; x: number; y: number; width: number; height: number; prompt: string }>
  >;
  cgs: Array<{ name: string; images: string[]; triggers: string[] }>;
} | null {
  if (!body || typeof body !== 'object') {
    return null;
  }

  const record = body as Record<string, unknown>;
  const name = asOptionalString(record.name)?.trim();
  const cardName = asOptionalString(record.cardName)?.trim();
  const accentColor = parseAccentColor(record.accentColor);
  const id = asOptionalString(record.id)?.trim() || undefined;
  const suggestedAffinityPositiveMaximum =
    typeof record.suggestedAffinityPositiveMaximum === 'number' && Number.isFinite(record.suggestedAffinityPositiveMaximum)
      ? Math.min(Math.max(Math.round(record.suggestedAffinityPositiveMaximum), 0), 120)
      : undefined;
  const suggestedAffinityNegativeMaximum =
    typeof record.suggestedAffinityNegativeMaximum === 'number' && Number.isFinite(record.suggestedAffinityNegativeMaximum)
      ? Math.min(Math.max(Math.round(record.suggestedAffinityNegativeMaximum), -120), 0)
      : undefined;
  const suggestedLustMaximum =
    typeof record.suggestedLustMaximum === 'number' && Number.isFinite(record.suggestedLustMaximum)
      ? Math.min(Math.max(Math.round(record.suggestedLustMaximum), 0), 100)
      : undefined;
  const characterNameFontId = normalizeDialogueQuoteFontId(record.characterNameFontId);
  const blipSound = asOptionalString(record.blipSound)?.trim() || '';
  const dialogueQuoteFontId = normalizeDialogueQuoteFontId(record.dialogueQuoteFontId);
  const dialogueQuoteAnimationPreset = normalizeDialogueQuoteAnimationPreset(record.dialogueQuoteAnimationPreset);
  const dialogueQuoteAnimationSpeed = normalizeDialogueQuoteAnimationSpeed(record.dialogueQuoteAnimationSpeed);
  const dialogueQuoteAnimationColor = normalizeDialogueQuoteAnimationColor(record.dialogueQuoteAnimationColor);

  if (!name || !cardName || !accentColor) {
    return null;
  }
  const characterNameColor = parseAccentColor(record.characterNameColor) || accentColor;

  const spritesRaw = record.sprites;
  if (!spritesRaw || typeof spritesRaw !== 'object') {
    return null;
  }

  const sprites: Record<string, string[]> = {};
  for (const expression of SPRITE_EXPRESSIONS) {
    const variants = parseImageVariants((spritesRaw as Record<string, unknown>)[expression]);
    if (!variants) {
      return null;
    }

    if (variants.length === 0) {
      sprites[expression] = [];
      continue;
    }

    sprites[expression] = variants;
  }

  const spriteDepthMapsRaw =
    record.spriteDepthMaps && typeof record.spriteDepthMaps === 'object' && !Array.isArray(record.spriteDepthMaps)
      ? (record.spriteDepthMaps as Record<string, unknown>)
      : {};
  const spriteDepthMaps: Record<string, string[]> = {};
  for (const expression of SPRITE_EXPRESSIONS) {
    const depthVariants = parseImageVariants(spriteDepthMapsRaw[expression]);
    if (!depthVariants) {
      return null;
    }
    spriteDepthMaps[expression] = depthVariants.slice(0, sprites[expression].length);
  }

  const spriteAnimationFramesRaw =
    record.spriteAnimationFrames && typeof record.spriteAnimationFrames === 'object' && !Array.isArray(record.spriteAnimationFrames)
      ? (record.spriteAnimationFrames as Record<string, unknown>)
      : {};
  const spriteAnimationFrames: Record<string, { closedEyes: string[]; openMouth: string[] }> = {};
  for (const expression of SPRITE_EXPRESSIONS) {
    const frames = parseSpriteAnimationFrames(spriteAnimationFramesRaw[expression], sprites[expression].length);
    if (!frames) {
      return null;
    }
    spriteAnimationFrames[expression] = frames;
  }

  const customReactionsRaw = Array.isArray(record.customReactions) ? record.customReactions : [];
  const customReactions: Array<{
    name: string;
    sprites: string[];
    depthMaps?: string[];
    animationFrames?: { closedEyes: string[]; openMouth: string[] };
    triggers: string[];
  }> = [];
  const customReactionNames = new Set<string>();
  const automaticGeneration = parseAutomaticGenerationSettings(record.automaticGeneration);

  for (const entry of customReactionsRaw) {
    if (!entry || typeof entry !== 'object') {
      return null;
    }

    const item = entry as Record<string, unknown>;
    const normalizedTriggers = (Array.isArray(item.triggers) ? item.triggers : [item.name])
      .map((trigger) => normalizeReactionName(asOptionalString(trigger)?.trim() || ''))
      .filter(Boolean)
      .filter((trigger, index, list) => list.indexOf(trigger) === index);
    const normalizedName = normalizedTriggers[0] || '';
    const sprites = parseImageVariants(item.sprites ?? item.sprite);
    const depthMaps = parseImageVariants(item.depthMaps ?? item.depthMap);

    if (!normalizedName || !/^[A-Z0-9 _-]{1,40}$/.test(normalizedName)) {
      return null;
    }

    if (!sprites || sprites.length === 0 || !depthMaps) {
      return null;
    }

    const animationFrames = parseSpriteAnimationFrames(item.animationFrames, sprites.length);
    if (!animationFrames) {
      return null;
    }

    if (normalizedTriggers.some((trigger) => customReactionNames.has(trigger))) {
      return null;
    }

    normalizedTriggers.forEach((trigger) => customReactionNames.add(trigger));
    customReactions.push({
      name: normalizedName,
      sprites,
      depthMaps: depthMaps.slice(0, sprites.length),
      animationFrames:
        animationFrames.closedEyes.some(Boolean) || animationFrames.openMouth.some(Boolean)
          ? animationFrames
          : undefined,
      triggers: normalizedTriggers,
    });
  }

  const spriteZonesRaw =
    record.spriteZones && typeof record.spriteZones === 'object' && !Array.isArray(record.spriteZones)
      ? (record.spriteZones as Record<string, unknown>)
      : {};
  const spriteZones: Record<
    string,
    Array<{ id: string; x: number; y: number; width: number; height: number; prompt: string }>
  > = {};

  for (const [expressionKey, zoneValue] of Object.entries(spriteZonesRaw)) {
    const normalizedKey = normalizeReactionName(expressionKey);
    if (!normalizedKey || !Array.isArray(zoneValue)) {
      continue;
    }

    const zones: Array<{ id: string; x: number; y: number; width: number; height: number; prompt: string }> = [];

    for (const zoneEntry of zoneValue) {
      if (!zoneEntry || typeof zoneEntry !== 'object') {
        return null;
      }

      const zoneRecord = zoneEntry as Record<string, unknown>;
      const id = asOptionalString(zoneRecord.id)?.trim() || '';
      const prompt = asOptionalString(zoneRecord.prompt)?.trim() || '';
      const x = typeof zoneRecord.x === 'number' && Number.isFinite(zoneRecord.x) ? zoneRecord.x : NaN;
      const y = typeof zoneRecord.y === 'number' && Number.isFinite(zoneRecord.y) ? zoneRecord.y : NaN;
      const width = typeof zoneRecord.width === 'number' && Number.isFinite(zoneRecord.width) ? zoneRecord.width : NaN;
      const height = typeof zoneRecord.height === 'number' && Number.isFinite(zoneRecord.height) ? zoneRecord.height : NaN;

      if (
        !id ||
        Number.isNaN(x) ||
        Number.isNaN(y) ||
        Number.isNaN(width) ||
        Number.isNaN(height) ||
        x < 0 ||
        x > 1 ||
        y < 0 ||
        y > 1 ||
        width <= 0 ||
        width > 1 ||
        height <= 0 ||
        height > 1 ||
        x + width > 1.000001 ||
        y + height > 1.000001
      ) {
        return null;
      }

      zones.push({
        id,
        x,
        y,
        width,
        height,
        prompt,
      });
    }

    if (zones.length > 0) {
      spriteZones[normalizedKey] = zones;
    }
  }

  const cgsRaw = Array.isArray(record.cgs) ? record.cgs : [];
  const cgs: Array<{ name: string; images: string[]; triggers: string[] }> = [];
  const cgNames = new Set<string>();

  for (const entry of cgsRaw) {
    if (!entry || typeof entry !== 'object') {
      return null;
    }

    const item = entry as Record<string, unknown>;
    const triggers = (Array.isArray(item.triggers) ? item.triggers : [item.name])
      .map((trigger) => normalizeReactionName(asOptionalString(trigger)?.trim() || ''))
      .filter(Boolean)
      .filter((trigger, index, list) => list.indexOf(trigger) === index);
    const name = triggers[0] || '';
    const images = parseImageVariants(item.images ?? item.image);

    if (!name || !images || images.length === 0) {
      return null;
    }

    if (triggers.some((trigger) => cgNames.has(trigger.toLowerCase()))) {
      return null;
    }

    triggers.forEach((trigger) => cgNames.add(trigger.toLowerCase()));
    cgs.push({ name, images, triggers });
  }

  return {
    id,
    name,
    cardName,
    accentColor,
    suggestedAffinityPositiveMaximum,
    suggestedAffinityNegativeMaximum,
    suggestedLustMaximum,
    characterNameFontId: characterNameFontId || undefined,
    characterNameColor,
    blipSound: /^[a-zA-Z0-9._-]+$/.test(blipSound) ? blipSound : undefined,
    dialogueQuoteFontId: dialogueQuoteFontId || undefined,
    dialogueQuoteAnimationPreset,
    dialogueQuoteAnimationSpeed,
    dialogueQuoteAnimationColor,
    sprites,
    spriteDepthMaps,
    spriteAnimationFrames,
    customReactions,
    automaticGeneration,
    spriteZones,
    cgs,
  };
}

function normalizeDialogueQuoteFontId(value: unknown): string {
  const normalized = `${value || ''}`.trim().toLowerCase();
  return /^[a-z0-9-]{1,80}$/.test(normalized) ? normalized : '';
}

function normalizeDialogueQuoteAnimationPreset(value: unknown): string {
  const normalized = `${value || ''}`.trim().toLowerCase();
  return ['disabled', 'glowing', 'crawlies', 'wave', 'flicker', 'glitch', 'echo'].includes(normalized)
    ? normalized
    : 'disabled';
}

function normalizeDialogueQuoteAnimationSpeed(value: unknown): number {
  const numeric = typeof value === 'number' ? value : Number.parseFloat(`${value || ''}`);
  if (!Number.isFinite(numeric)) {
    return 1;
  }

  return Math.min(Math.max(numeric, 0.25), 3);
}

function normalizeDialogueQuoteAnimationColor(value: unknown): string {
  const parsed = parseAccentColor(value);
  return parsed ? parsed.toUpperCase() : '#F6F0E6';
}

type ParsedCharacterPayload = NonNullable<ReturnType<typeof parseCharacterPayload>>;

function hasRetainedCharacterAssets(payload: ParsedCharacterPayload): boolean {
  return (
    Object.values(payload.sprites).some((variants) => variants.some(isRetainedCharacterAssetToken)) ||
    Object.values(payload.spriteDepthMaps).some((variants) => variants.some(isRetainedCharacterAssetToken)) ||
    Object.values(payload.spriteAnimationFrames).some(
      (frames) =>
        frames.closedEyes.some(isRetainedCharacterAssetToken) ||
        frames.openMouth.some(isRetainedCharacterAssetToken),
    ) ||
    payload.customReactions.some(
      (reaction) =>
        reaction.sprites.some(isRetainedCharacterAssetToken) ||
        (reaction.depthMaps || []).some(isRetainedCharacterAssetToken) ||
        (reaction.animationFrames?.closedEyes || []).some(isRetainedCharacterAssetToken) ||
        (reaction.animationFrames?.openMouth || []).some(isRetainedCharacterAssetToken),
    ) ||
    payload.cgs.some((cg) => cg.images.some(isRetainedCharacterAssetToken))
  );
}

function resolveImageVariants(
  incomingVariants: string[],
  existingVariants: string[] | undefined,
): string[] | null {
  const resolved = incomingVariants.map((variant, index) => {
    if (!isRetainedCharacterAssetToken(variant)) {
      return variant;
    }

    return existingVariants?.[index] || '';
  });

  return resolved.every(isImageAssetReference) ? resolved : null;
}

function getTriggerKey(entity: { name: string; triggers?: string[] }): string {
  const triggers = (entity.triggers && entity.triggers.length > 0 ? entity.triggers : [entity.name])
    .map((trigger) => normalizeReactionName(trigger))
    .filter(Boolean);
  return triggers.join('|');
}

function findMatchingEntity<T extends { name: string; triggers?: string[] }>(
  entities: T[],
  target: { name: string; triggers?: string[] },
): T | undefined {
  const targetKey = getTriggerKey(target);
  return entities.find((entity) => getTriggerKey(entity) === targetKey);
}

async function resolveRetainedCharacterAssets(
  payload: ParsedCharacterPayload,
): Promise<ParsedCharacterPayload | null> {
  if (!hasRetainedCharacterAssets(payload)) {
    return payload;
  }

  if (!payload.id) {
    return null;
  }

  const state = await getStudioState();
  const existingCharacter = state.characters.find((character) => character.id === payload.id);
  if (!existingCharacter) {
    return null;
  }

  const sprites: Record<string, string[]> = {};
  for (const expression of SPRITE_EXPRESSIONS) {
    const resolvedVariants = resolveImageVariants(payload.sprites[expression] || [], existingCharacter.sprites[expression]);
    if (!resolvedVariants) {
      return null;
    }
    sprites[expression] = resolvedVariants;
  }

  const spriteDepthMaps: Record<string, string[]> = {};
  for (const expression of SPRITE_EXPRESSIONS) {
    const resolvedVariants = resolveImageVariants(
      payload.spriteDepthMaps[expression] || [],
      existingCharacter.spriteDepthMaps[expression],
    );
    if (!resolvedVariants) {
      return null;
    }
    spriteDepthMaps[expression] = resolvedVariants.slice(0, sprites[expression].length);
  }

  const spriteAnimationFrames: Record<string, { closedEyes: string[]; openMouth: string[] }> = {};
  for (const expression of SPRITE_EXPRESSIONS) {
    const existingFrames = existingCharacter.spriteAnimationFrames?.[expression];
    const incomingFrames = payload.spriteAnimationFrames[expression] || { closedEyes: [], openMouth: [] };
    const closedEyes = resolveImageVariants(incomingFrames.closedEyes || [], existingFrames?.closedEyes);
    const openMouth = resolveImageVariants(incomingFrames.openMouth || [], existingFrames?.openMouth);
    if (!closedEyes || !openMouth) {
      return null;
    }
    spriteAnimationFrames[expression] = {
      closedEyes: closedEyes.slice(0, sprites[expression].length),
      openMouth: openMouth.slice(0, sprites[expression].length),
    };
  }

  const customReactions = payload.customReactions.map((reaction) => {
    const existingReaction = findMatchingEntity(existingCharacter.customReactions, reaction);
    const sprites = resolveImageVariants(reaction.sprites, existingReaction?.sprites);
    const depthMaps = reaction.depthMaps
      ? resolveImageVariants(reaction.depthMaps, existingReaction?.depthMaps)
      : reaction.depthMaps;
    const animationFrames = reaction.animationFrames
      ? {
          closedEyes: resolveImageVariants(reaction.animationFrames.closedEyes, existingReaction?.animationFrames?.closedEyes),
          openMouth: resolveImageVariants(reaction.animationFrames.openMouth, existingReaction?.animationFrames?.openMouth),
        }
      : reaction.animationFrames;

    if (
      !sprites ||
      !depthMaps ||
      (animationFrames && (!animationFrames.closedEyes || !animationFrames.openMouth))
    ) {
      return null;
    }

    return {
      ...reaction,
      sprites,
      depthMaps,
      animationFrames: animationFrames
        ? {
            closedEyes: animationFrames.closedEyes!.slice(0, sprites.length),
            openMouth: animationFrames.openMouth!.slice(0, sprites.length),
          }
        : animationFrames,
    };
  });

  if (customReactions.some((reaction) => !reaction)) {
    return null;
  }

  const cgs = payload.cgs.map((cg) => {
    const existingCg = findMatchingEntity(existingCharacter.cgs, cg);
    const images = resolveImageVariants(cg.images, existingCg?.images);
    return images ? { ...cg, images } : null;
  });

  if (cgs.some((cg) => !cg)) {
    return null;
  }

  return {
    ...payload,
    sprites,
    spriteDepthMaps,
    spriteAnimationFrames,
    customReactions: customReactions as ParsedCharacterPayload['customReactions'],
    cgs: cgs as ParsedCharacterPayload['cgs'],
  };
}

function parseScenarioPayload(
  body: unknown,
): {
  id?: string;
  name: string;
  description: string;
  startMessage: string;
  specialInstructions: string;
  characterId: string;
  bannerDataUrl?: string;
  startSceneId?: string;
  startingPoints?: Array<{
    id?: string;
    name: string;
    sceneId: string;
    startMessage: string;
    specialInstructions: string;
  }>;
  scenes: Array<{
    id?: string;
    name: string;
    backgroundDataUrl: string;
    backgroundDepthMapDataUrl?: string;
    bgmDataUrl?: string;
    ambientNoiseDataUrl?: string;
    ambientNoisePresetId?: string;
    ambientNoiseMuffled?: boolean;
    weatherPreset?: SceneWeatherPreset;
    triggerWords: string[];
  }>;
} | null {
  if (!body || typeof body !== 'object') {
    return null;
  }

  const record = body as Record<string, unknown>;
  const name = asOptionalString(record.name)?.trim();
  const description = asOptionalString(record.description)?.trim() || '';
  const startMessage = asOptionalString(record.startMessage)?.trim() || '';
  const specialInstructions = asOptionalString(record.specialInstructions)?.trim() || '';
  const characterId = asOptionalString(record.characterId)?.trim();
  const bannerDataUrl = asOptionalString(record.bannerDataUrl)?.trim() || '';
  const startSceneId = asOptionalString(record.startSceneId)?.trim() || undefined;
  const id = asOptionalString(record.id)?.trim() || undefined;

  if (!name || !characterId) {
    return null;
  }

  if (bannerDataUrl && !isImageAssetReference(bannerDataUrl)) {
    return null;
  }

  const scenesRaw = Array.isArray(record.scenes) ? record.scenes : [];
  const scenes: Array<{
    id?: string;
    name: string;
    backgroundDataUrl: string;
    backgroundDepthMapDataUrl?: string;
    bgmDataUrl?: string;
    ambientNoiseDataUrl?: string;
    ambientNoisePresetId?: string;
    ambientNoiseMuffled?: boolean;
    weatherPreset?: SceneWeatherPreset;
    triggerWords: string[];
  }> = [];

  for (const scene of scenesRaw) {
    if (!scene || typeof scene !== 'object') {
      continue;
    }

    const sceneRecord = scene as Record<string, unknown>;
    const sceneId = asOptionalString(sceneRecord.id)?.trim() || undefined;
    const sceneName = asOptionalString(sceneRecord.name)?.trim();
    const backgroundDataUrl = asOptionalString(sceneRecord.backgroundDataUrl)?.trim();
    const backgroundDepthMapDataUrl =
      asOptionalString(sceneRecord.backgroundDepthMapDataUrl ?? sceneRecord.depthMapDataUrl)?.trim() || '';
    const bgmDataUrl = asOptionalString(sceneRecord.bgmDataUrl)?.trim() || '';
    const ambientNoiseDataUrl = asOptionalString(sceneRecord.ambientNoiseDataUrl)?.trim() || '';
    const ambientNoisePresetId = asOptionalString(sceneRecord.ambientNoisePresetId)?.trim() || '';
    const ambientNoiseMuffled = sceneRecord.ambientNoiseMuffled === true;
    const weatherPreset = parseSceneWeatherPreset(sceneRecord.weatherPreset);
    const triggerWords = parseTriggerWords(sceneRecord.triggerWords);

    if (!sceneName || !backgroundDataUrl || !isImageAssetReference(backgroundDataUrl)) {
      return null;
    }

    if (bgmDataUrl && !isAudioAssetReference(bgmDataUrl)) {
      return null;
    }

    if (ambientNoiseDataUrl && !isAudioAssetReference(ambientNoiseDataUrl)) {
      return null;
    }

    if (backgroundDepthMapDataUrl && !isImageAssetReference(backgroundDepthMapDataUrl)) {
      return null;
    }

    scenes.push({
      id: sceneId,
      name: sceneName,
      backgroundDataUrl,
      backgroundDepthMapDataUrl: backgroundDepthMapDataUrl || undefined,
      bgmDataUrl: bgmDataUrl || undefined,
      ambientNoiseDataUrl: ambientNoiseDataUrl || undefined,
      ambientNoisePresetId: ambientNoisePresetId || undefined,
      ambientNoiseMuffled,
      weatherPreset,
      triggerWords,
    });
  }

  if (scenes.length === 0) {
    return null;
  }

  const validSceneIds = new Set(scenes.map((scene) => scene.id).filter(Boolean) as string[]);
  const startingPointsRaw = Array.isArray(record.startingPoints) ? record.startingPoints : [];
  const startingPoints: Array<{
    id?: string;
    name: string;
    sceneId: string;
    startMessage: string;
    specialInstructions: string;
  }> = [];
  for (const rawPoint of startingPointsRaw) {
    if (!rawPoint || typeof rawPoint !== 'object') {
      continue;
    }

    const point = rawPoint as Record<string, unknown>;
    const pointId = asOptionalString(point.id)?.trim() || undefined;
    const pointName = asOptionalString(point.name)?.trim() || '';
    const pointSceneId = asOptionalString(point.sceneId)?.trim() || '';
    const pointStartMessage = asOptionalString(point.startMessage)?.trim() || '';
    const pointSpecialInstructions = asOptionalString(point.specialInstructions)?.trim() || '';
    if (!pointSceneId || !validSceneIds.has(pointSceneId)) {
      continue;
    }

    startingPoints.push({
      id: pointId,
      name: pointName || scenes.find((scene) => scene.id === pointSceneId)?.name || `Start ${startingPoints.length + 1}`,
      sceneId: pointSceneId,
      startMessage: pointStartMessage,
      specialInstructions: pointSpecialInstructions,
    });

    if (startingPoints.length >= 5) {
      break;
    }
  }

  if (startingPoints.length === 0 && scenes[0]?.id) {
    const fallbackSceneId = validSceneIds.has(startSceneId || '') ? startSceneId! : scenes[0].id;
    startingPoints.push({
      name: scenes.find((scene) => scene.id === fallbackSceneId)?.name || 'Default',
      sceneId: fallbackSceneId,
      startMessage: startMessage || '',
      specialInstructions,
    });
  }

  if (!startingPoints[0]?.startMessage.trim()) {
    return null;
  }

  return {
    id,
    name,
    description,
    startMessage,
    specialInstructions,
    characterId,
    bannerDataUrl: bannerDataUrl || undefined,
    startSceneId: scenes.some((scene) => scene.id === startSceneId) ? startSceneId : scenes[0]?.id,
    startingPoints,
    scenes,
  };
}

function parseRunCreatePayload(
  body: unknown,
): {
  scenarioId: string;
  title: string;
  messages: ScenarioRunMessage[];
  currentSceneId?: string;
  startingPointId?: string;
} | null {
  if (!body || typeof body !== 'object') {
    return null;
  }

  const record = body as Record<string, unknown>;
  const scenarioId = asOptionalString(record.scenarioId)?.trim();
  const title = asOptionalString(record.title)?.trim();
  const messages = parseMessagesArray(record.messages);
  const currentSceneId = asOptionalString(record.currentSceneId)?.trim() || undefined;
  const startingPointId = asOptionalString(record.startingPointId)?.trim() || undefined;

  if (!scenarioId || !title || !messages) {
    return null;
  }

  return {
    scenarioId,
    title,
    messages,
    currentSceneId,
    startingPointId,
  };
}

function slugifyFileName(value: string): string {
  const normalized = value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');

  return normalized || 'package';
}

