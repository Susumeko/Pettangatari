  const lightingColorRaw = asOptionalString(record.lightingColor)?.trim().toUpperCase() || '';
  const lightingColor: CharacterAutomaticGenerationRecord['lightingColor'] =
    lightingColorRaw === 'BLUE' ||
    lightingColorRaw === 'RED' ||
    lightingColorRaw === 'PURPLE' ||
    lightingColorRaw === 'ORANGE' ||
    lightingColorRaw === 'YELLOW' ||
    lightingColorRaw === 'GREEN' ||
    lightingColorRaw === 'PINK'
      ? lightingColorRaw
      : 'NEUTRAL';
  const breastSizeRaw = asOptionalString(record.breastSize)?.trim().toUpperCase() || '';
  const breastSize: CharacterAutomaticGenerationRecord['breastSize'] =
    breastSizeRaw === 'FLAT' ||
    breastSizeRaw === 'SMALL' ||
    breastSizeRaw === 'LARGE' ||
    breastSizeRaw === 'HUGE' ||
    breastSizeRaw === 'GIGANTIC'
