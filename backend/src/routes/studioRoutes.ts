router.put('/comfy/connection', async (request, response) => {
  const body = request.body as Record<string, unknown> | null;
  const requestedBaseUrl = typeof body?.baseUrl === 'string' ? body.baseUrl : '';

  let normalizedBaseUrl = '';
  try {
    normalizedBaseUrl = normalizeComfyBaseUrl(requestedBaseUrl);
  } catch (error) {
    return response.status(400).json({
      error: error instanceof Error ? error.message : 'Invalid ComfyUI API address.',
    });
  }

  const previousBaseUrl = getComfyBaseUrl();
  setComfyBaseUrl(normalizedBaseUrl, { persist: false });

  try {
    const status = await getComfyStatus();
    if (!status.online) {
      throw new Error(status.error || 'ComfyUI is unreachable.');
    }

    setComfyBaseUrl(normalizedBaseUrl);
    return response.json({
      baseUrl: normalizedBaseUrl,
      online: true,
    });
  } catch (error) {
    setComfyBaseUrl(previousBaseUrl, { persist: false });
    return response.status(502).json({
      baseUrl: previousBaseUrl,
      online: false,
      error: error instanceof Error ? error.message : 'Failed to connect to ComfyUI.',
    });
  }
});

